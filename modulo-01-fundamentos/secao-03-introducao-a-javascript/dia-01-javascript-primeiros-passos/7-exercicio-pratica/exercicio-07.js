// Defina três números em constantes e retorne true se pelo menos uma das três for par. Caso contrário, ele deve retornar false.

const num1 = 5;
const num2 = 10;
const num3 = 15;

let isPar = false

if (num1 % 2 === 0 || num2 % 2 === 0 || num3 % 2 === 0) {
    isPar = !false
}

console.log(isPar);

// Defina três números em constantes e retorne true se pelo menos uma das três for ímpar. Caso contrário, ele deve retornar false.

const num4 = 20;
const num5 = 25;
const num6 = 30;

let isImpar = false

if (num4 % 2 !== 0 || num5 % 2 !== 0 || num6 % 2 !== 0) {
    isImpar = !false
}

console.log(isImpar);

